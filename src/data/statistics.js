// ฟังก์ชันสร้างน้ำหนักจากสถิติ
export function buildWeightedPool(frequencyMap) {
  const pool = []
  for (const [num, freq] of Object.entries(frequencyMap)) {
    const weight = Math.max(freq, 1)
    for (let i = 0; i < weight; i++) {
      pool.push(num)
    }
  }
  return pool
}
