// คำนวณ frequency จากประวัติจริง
export function calcFrequencyFromHistory() {
  const two = {}
  const three = {}

  for (let i = 0; i <= 99; i++) two[String(i).padStart(2, '0')] = 0
  for (let i = 0; i <= 999; i++) three[String(i).padStart(3, '0')] = 0

  for (const draw of LOTTERY_DRAWS) {
    two[draw.back2] = (two[draw.back2] || 0) + 1
    three[draw.back3] = (three[draw.back3] || 0) + 1
  }

  return { twoDigit: two, threeDigit: three }
}

